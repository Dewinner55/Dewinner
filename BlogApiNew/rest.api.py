# Логин 3 этапа
# идентификация - процедура, при помощи которой определяется есть ли этот субъект в системе в базе данных.
# аутентификация - процедура проверки подлинности, например, проверка подлинности пользовотеля путем сравнения введенного им пароля с паролем, сохраненным в бд.
# авторизация - процедура предоставления определенному лицу прав на выполнение определенных действий.
